package ejercicio5;

public class Contadores {

	public static void main(String[] args) {
		int valor= 0;
		valor=23;
		valor +=10;
		System.out.println(--valor);//32
		System.out.println(++valor);//33
		System.out.println(valor++);//33
		System.out.println(valor--);//34
		System.out.println(valor);//33
	}

}
