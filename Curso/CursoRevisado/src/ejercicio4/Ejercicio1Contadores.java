package ejercicio4;

public class Ejercicio1Contadores {
	/**
	 * Intenta predecir que resultado daría el siguiente código; 
	 * después puedes escribirlo y ejecutarlo para comprobar el resultado: 
	 * @param args
	 */

	public static void main(String[] args) {
		int valor= 0;
		valor=23;
		valor +=10;
		System.out.println(--valor);//32
		System.out.println(++valor);//33
		System.out.println(valor++);//33
		System.out.println(valor--);//34
		System.out.println(valor);//33
	}

}
